'''
Faça um Programa que pergunte quanto você ganha por hora e o
número de horas trabalhadas no mês.
Calcule e mostre o total do seu salário no referido mês.
'''

salario = float(input('Informe quanto você recebe por hora: '))
sal_hora = float(input('Informe quantas horas trabalhadas neste mês: '))

print(f'\nSeu salário mensal é: R${salario * sal_hora}')

